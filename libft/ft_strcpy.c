#include "libft.h"

char	*ft_strcpy(char *s1, char *s2)
{
	char	*str;

	str = (char *)s1;
	while (*s2)
	{
		*str = *s2;
		s2++;
		str++;
	}
	*str = '\0';
	return (s1);
}
